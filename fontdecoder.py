
def decodeSymbol(raster):
    superVal = 0
    for u in range(len(raster)):
        superVal += raster[u] * 2**(8*u)

    if superVal in DECIPHER:
        return DECIPHER[superVal]
    else:
        return f"[{superVal}]"


